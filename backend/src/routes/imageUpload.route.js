import express from "express";
import { upload, uploadImage } from "../controllers/imageUpload.controller.js";

const router = express.Router();

/**
 * @swagger
 * /image/upload:
 *   post:
 *     tags: [imgae upload]
 *     summary: Upload an image and get its URL
 *     description: Uploads an image to Cloudinary and returns the URL of the uploaded image.
 *     requestBody:
 *       content:
 *         multipart/form-data:
 *           schema:
 *             type: object
 *             properties:
 *               image:
 *                 type: string
 *                 format: binary
 *             required:
 *               - image
 *     responses:
 *       200:
 *         description: OK
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 url:
 *                   type: string
 *                   description: The URL of the uploaded image
 *       400:
 *         description: Bad Request
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 message:
 *                   type: string
 *                   description: The error message
 *       500:
 *         description: Internal Server Error
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 message:
 *                   type: string
 *                   description: The error message
 */
router.post("/upload", upload.single("image"), uploadImage);

export default router;
